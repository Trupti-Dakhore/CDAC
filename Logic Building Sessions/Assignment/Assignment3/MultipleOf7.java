class MultipleOf7
{
public static void main(String[] args)
  {
     int i;
     System.out.println("Multiple of 7 between 1 to 100");
     for(i=1;i<=100;i++)
     {
	   if(i%7==0)
	    {
	      System.out.print(" "+i);  
        }
   
     }
  
  }	
}
/*
Output:Multiple of 7 between 1 to 100
7 14 21 28 35 42 49 56 63 70 77 84 91 98
*/
